CREATE TABLE FRAMEWORK (
	FRAMEWORLID INTEGER NOT NULL,
	NAME VARCHAR(50),
	AUTHOR VARCHAR(50),
	INFO LONG VARCHAR,
	VERSION VARCHAR(10),
	PRIMARY KEY (FRAMEWORLID)
);

CREATE TABLE FRAMEWORKITEM (
	FRAMEWORKID INTEGER NOT NULL,
	FRAMEWORKITEMID INTEGER NOT NULL,
	DESCRIPTION VARCHAR(50),
	COMPLETE BOOLEAN,
	PRIMARY KEY (FRAMEWORKID,FRAMEWORKITEMID)
);

CREATE TABLE ICONS (
	ICONID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
	PATH VARCHAR(255),
	PRIMARY KEY (ICONID)
);

CREATE TABLE PROJECT (
	PROJECTID INTEGER NOT NULL,
	NAME VARCHAR(80),
	CUSTOMER VARCHAR(50),
	STARTDATE DATE,
	ENDDATE DATE,
	AUTHOR VARCHAR(50),
	WORKDIR VARCHAR(255),
	FRAMEWORKID INTEGER,
	PRIMARY KEY (PROJECTID)
);

CREATE TABLE PROJECTTOOL (
	PROJECTID INTEGER NOT NULL,
	TOOLID INTEGER NOT NULL,
	PRIMARY KEY (PROJECTID,TOOLID)
);

CREATE TABLE RISK (
	RISKID INTEGER NOT NULL,
	DESCRIPTION VARCHAR(15),
	ICONID INTEGER,
	PRIMARY KEY (RISKID)
);

CREATE TABLE TARGET (
	PROJECTID INTEGER NOT NULL,
	TARGETID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
	NAME VARCHAR(50),
	TARGETTYPEID INTEGER,
	PRIMARY KEY (PROJECTID,TARGETID)
);

CREATE TABLE TARGETITEM (
	PROJECTID INTEGER NOT NULL,
	TARGETID INTEGER NOT NULL,
	TARGETITEMID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
	RISKID INTEGER,
	PORT INTEGER,
	PROTOCOL VARCHAR(10),
	NAME VARCHAR(50),
	CONTENT LONG VARCHAR,
	PRIMARY KEY (PROJECTID,TARGETID,TARGETITEMID)
);

CREATE TABLE TARGETTYPE (
	TARGETTYPEID INTEGER NOT NULL,
	NAME VARCHAR(50),
	VERSION VARCHAR(50),
	ICONID INTEGER,
	PRIMARY KEY (TARGETTYPEID)
);

CREATE TABLE TOOL (
	TOOLID INTEGER NOT NULL,
	NAME VARCHAR(50),
	VERSION VARCHAR(10),
	AUTHOR VARCHAR(50),
	INFO LONG VARCHAR,
	COMMAND LONG VARCHAR,
	WORKDIR VARCHAR(255),
	ENABLED BOOLEAN,
	PRIMARY KEY (TOOLID)
);

